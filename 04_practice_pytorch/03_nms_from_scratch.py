# -*- coding: utf-8 -*-
"""
关卡 3 PyTorch版: 手写 NMS 算法 + 可视化对比

═══════════════════════════════════════════════════════════════════════════════
NMS 解决了什么问题
═══════════════════════════════════════════════════════════════════════════════

  YOLO 检测完一张图，同一个目标上可能叠了好几个框:
    框A (置信度 0.95)
    框B (置信度 0.82)    ← 三个框高度重叠，都在同一只猫上
    框C (置信度 0.71)

  你想每个目标只保留一个最优框 → NMS 就是做这个"去重"的。

  NMS = Non-Maximum Suppression = 非极大值抑制
  翻译: "不是最高分的框，就干掉"

═══════════════════════════════════════════════════════════════════════════════
这个脚本做的事
═══════════════════════════════════════════════════════════════════════════════

  1. 手工造 8 个假框（模拟 YOLO 原始输出，有重叠、有不同类别）
  2. 手写 IoU 计算（算两个框重叠多少）
  3. 手写传统 NMS（重叠 > 阈值 → 删）
  4. 手写 Soft-NMS（重叠 > 阈值 → 降分，不删）
  5. 画 3 张对比图

  不依赖 torchvision.ops.nms，纯 numpy 实现。面试手撕代码就是撕这个。

═══════════════════════════════════════════════════════════════════════════════
传统 NMS vs Soft-NMS
═══════════════════════════════════════════════════════════════════════════════

  传统 NMS:  IoU > 阈值 → 直接删除 (置信度变 0)
            问题: 密集场景下两个同类目标靠很近，IoU 也高，会被误删

  Soft-NMS:  IoU > 阈值 → 降分（乘一个衰减系数）
            score_new = score × e^(-IoU² / σ)
            好处: 框还在，只是分低了。跟更高分的框比不过，但不会被错杀
"""

import numpy as np
import matplotlib
matplotlib.use("TkAgg")
import matplotlib.pyplot as plt
import matplotlib.patches as patches  # patches: 画矩形框的模块
matplotlib.rcParams["font.sans-serif"] = ["Microsoft YaHei"]


# ═════════════════════════════════════════════════════════════════════════════
# 1. 造假框 — 模拟 YOLO 的原始输出
# ═════════════════════════════════════════════════════════════════════════════
# 每个框 6 个数: [x1, y1, x2, y2, 置信度, 类别]
#   前 4 个: 左上角 + 右下角 (xyxy 格式)
#   第 5 个: 置信度 (0~1, 模型认为自己多确定这是个目标)
#   第 6 个: 类别编号 (0=猫, 1=狗)
boxes = np.array([
    # 目标A: 猫, 位置(50,50)~(150,150), 模型给了 3 个高度重叠的框
    #         置信度各不相同 → NMS 应该只保留 0.95 那个
    [50, 50,   150, 150,  0.95, 0],
    [60, 55,   145, 148,  0.82, 0],   # 和上面那个 IoU 很高 → 会被删
    [45, 60,   155, 140,  0.71, 0],   # 同上
    # 目标B: 另一只猫, 位置偏右(130,40)~(220,140), 离目标A很近但不完全重叠
    [130, 40,  220, 140,  0.88, 0],
    [125, 45,  215, 135,  0.65, 0],   # 和目标B的第1个框重叠 → 会被删
    # 目标C: 狗, 位置(250,60)~(350,160), 跟猫离很远 → 不会受影响
    [250, 60,  350, 160,  0.91, 1],
    [255, 55,  345, 165,  0.78, 1],   # 和目标C的第1个框重叠 → 会被删
    # 背景误检: 置信度 0.42, 位置比较偏 → 正常的 conf 过滤就可能删掉它
    [300, 200, 340, 240,  0.42, 0],
])
# 类别名和颜色映射（方便画图时区分猫和狗）
names = {0: "猫", 1: "狗"}
colors = {0: "blue", 1: "orange"}


# ═════════════════════════════════════════════════════════════════════════════
# 2. 手写 IoU 计算
# ═════════════════════════════════════════════════════════════════════════════
def compute_iou(b1, b2):
    """
    计算两个框的 IoU (Intersection over Union = 交并比)

    IoU = 两个框的交集面积 / 两个框的并集面积
    值域: 0 (完全不重叠) ~ 1 (完全重叠)

    参数: b1, b2 都是 [x1, y1, x2, y2, ...] 格式
         前 4 个是左上角和右下角坐标
    """
    # ── 交集: 两个矩形重叠部分的左上角 + 右下角 ──
    # 重叠区域左上角 = 两个左上角中取最大值（靠右、靠下的那个）
    #   max(b1.x1, b2.x1): 哪边的左边界更靠右 → 重叠区域的左边界
    x1 = max(b1[0], b2[0])
    y1 = max(b1[1], b2[1])
    # 重叠区域右下角 = 两个右下角中取最小值（靠左、靠上的那个）
    x2 = min(b1[2], b2[2])
    y2 = min(b1[3], b2[3])

    # 交集面积 = 宽 × 高
    # max(0, ...): 如果不重叠 (x2<x1 或 y2<y1) → 交集为 0
    inter = max(0, x2 - x1) * max(0, y2 - y1)

    # ── 各自面积 ──
    a1 = (b1[2] - b1[0]) * (b1[3] - b1[1])  # 框1: (x2-x1) * (y2-y1)
    a2 = (b2[2] - b2[0]) * (b2[3] - b2[1])  # 框2

    # ── IoU ──
    # 并集 = 面积1 + 面积2 - 交集（交集算了两次所以减一次）
    # + 1e-8 = 加一个极小值防止除以 0
    return inter / (a1 + a2 - inter + 1e-8)


# ═════════════════════════════════════════════════════════════════════════════
# 3. 手写传统 NMS
# ═════════════════════════════════════════════════════════════════════════════
def nms(boxes, iou_thresh=0.5):
    """
    传统 NMS: 同一个目标只保留置信度最高的框。

    算法流程:
      1. 所有框按置信度从高到低排序
      2. 取出最高分的框 → 加入"保留"列表
      3. 剩下的框逐个跟它比 IoU:
           IoU > 阈值 → 认为是同一个目标的重叠框 → 删除
           IoU ≤ 阈值 → 认为是不同目标 → 保留
      4. 在剩下的框里重复 2-3，直到没框了

    参数:
      boxes:    [N, 6] 的 numpy 数组, 每行 [x1,y1,x2,y2,conf,cls]
      iou_thresh: IoU 阈值, 默认 0.5。调低→删更狠→框少。调高→删更松→框多

    返回: 保留的框的索引列表
    """
    # ── 步骤1: 按置信度降序排列 ──
    # boxes[:,4] = 所有框的第 5 列 (置信度)
    # np.argsort(...) = 返回从小到大排序的索引
    # [::-1] = 反转 → 从大到小 (降序)
    # 例: boxes 置信度 = [0.95, 0.82, 0.71, 0.88, ...]
    #     argsort → [2, 1, 3, 0, ...]  (从小到大)
    #     反转    → [0, 3, 1, 2, ...]  (从大到小)
    order = np.argsort(boxes[:, 4])[::-1]

    keep = []  # 保留的框的索引

    while len(order) > 0:
        # ── 步骤2: 当前最高分的框 → 保留 ──
        cur = order[0]
        keep.append(cur)

        if len(order) == 1:
            break  # 只剩一个，没得比了

        # ── 步骤3: 算最高分框和其余每个框的 IoU ──
        # order[1:] = 除了最高分外的其他框
        ious = [compute_iou(boxes[cur], boxes[i]) for i in order[1:]]

        # 只保留 IoU ≤ 阈值的框
        # np.array(ious) <= iou_thresh: 返回 bool 数组 [True, False, True, ...]
        # order[1:][...]: 用 bool 数组过滤 → 只保留 True 对应的索引
        order = order[1:][np.array(ious) <= iou_thresh]

    return keep


# ═════════════════════════════════════════════════════════════════════════════
# 4. 手写 Soft-NMS
# ═════════════════════════════════════════════════════════════════════════════
def soft_nms(boxes, iou_thresh=0.5, sigma=0.5):
    """
    Soft-NMS: 不删框，而是给重叠框的置信度"打折"。

    和传统 NMS 的区别:
      传统: IoU > 阈值 → 直接删
      Soft: IoU > 阈值 → score *= e^(-IoU²/σ)  → 降分但不归零

    降分公式: score_new = score × exp(-IoU² / sigma)
      例: IoU=0.6, sigma=0.5
          e^(-0.36/0.5) = e^(-0.72) = 0.487
          → 置信度打 4.9 折

      IoU 越大 → 打折越狠，但不归零
      低分框如果下一轮不再和高分框竞争，还是有机会被保留的

    好处: 密集场景下两个同类目标挨得近，传统 NMS 可能误删其中一个
          Soft-NMS 只降分不删 → 另一个目标还有机会

    参数:
      sigma: 控制衰减速度。越大 → 降分越温和。默认 0.5
    """
    # .copy(): 复制一份，不破坏原始 boxes（因为要修改置信度）
    # .astype(float): 确保置信度列可以存小数
    boxes = boxes.copy().astype(float)

    # np.argsort 返回 numpy 数组, list() 转成 Python 列表方便 pop
    order = list(np.argsort(boxes[:, 4])[::-1])

    keep = []

    while len(order) > 0:
        # pop(0): 弹出第 0 个元素（最高分），同时从 order 中移除
        cur = order.pop(0)
        keep.append(cur)

        # 遍历 order 里剩下的所有框
        for idx in list(order):  # list(order) = 浅拷贝, 因为遍历时要修改 order
            iou_val = compute_iou(boxes[cur], boxes[idx])

            # 重叠超过阈值 → 降分（不删除）
            if iou_val >= iou_thresh:
                # exp(-iou² / sigma): 高斯衰减函数
                #   iou=0.5 → e^(-0.25/0.5) = e^(-0.5)  = 0.607 → 打 6 折
                #   iou=0.9 → e^(-0.81/0.5) = e^(-1.62) = 0.198 → 打 2 折
                boxes[idx, 4] *= np.exp(-iou_val * iou_val / sigma)

                # 注意: idx 没有被从 order 中移除！
                # 只是分降低了，下一轮可能因为分太低自然被淘汰
                # 也可能分还是够高，被保留（密集场景下两个同类目标都存活）

    return keep


# ═════════════════════════════════════════════════════════════════════════════
# 5. 画 3 张对比图
# ═════════════════════════════════════════════════════════════════════════════
# 目标: 一张画布并排 3 张子图，一眼看出 NMS 前后的差别
#
# 画布结构:
#   ┌──────────────┬──────────────┬──────────────┐
#   │   子图 0     │   子图 1     │   子图 2     │
#   │  NMS 前      │  传统 NMS    │  Soft-NMS    │
#   │  8 框全留    │  保留 4 框   │  保留 8 框   │
#   └──────────────┴──────────────┴──────────────┘

# ── plt.subplots(行数, 列数, figsize=(宽英寸, 高英寸)) ──
# 返回:
#   fig  = 整张画布对象（控制全局：总标题、保存图片、显示窗口）
#   axes = 3 个子图的数组: [ax0, ax1, ax2]
#          ax0=左图  ax1=中图  ax2=右图
#   每个 ax 就是一个独立的"小画布"，可以独立画东西、设标题
fig, axes = plt.subplots(1, 3, figsize=(16, 5))

# 3 张子图各自的标题
titles = ["NMS 前 (8框)", "传统 NMS IoU=0.5", "Soft-NMS IoU=0.5"]

# ── zip: 把 3 组数据"拉链"绑定 ──
#   第 1 次循环: ax=ax0, title="NMS 前", keep=[0,1,2,3,4,5,6,7] ← 全保留
#   第 2 次循环: ax=ax1, title="传统 NMS", keep=[0,5,3,7]        ← nms() 返回
#   第 3 次循环: ax=ax2, title="Soft-NMS", keep=[0,5,3,1,6,4,7,2] ← soft_nms() 返回
#
#   3 个 keep 怎么来的:
#     list(range(8))    → [0,1,2,3,4,5,6,7]  Python 列表，8 个全在 = 不删
#     nms(boxes, 0.5)   → [0,5,3,7]          NMS 算法返回的保留索引
#     soft_nms(boxes, 0.5) → [0,5,3,1,6,4,7,2]  Soft-NMS 返回（8 个全在）
for ax, title, keep in zip(axes, titles,
    [list(range(8)), nms(boxes, 0.5), soft_nms(boxes, 0.5)]):

    # ── 设定坐标轴范围 ──
    # X 轴 0~400（容纳最右边的框 x2=350）
    # Y 轴 0~300（容纳最下边的框 y2=240）
    ax.set_xlim(0, 400)
    ax.set_ylim(0, 300)

    # invert_yaxis(): 翻转 Y 轴
    #   matplotlib 默认: y=0 在下面，y=300 在上面（数学坐标系）
    #   图片坐标系:      y=0 在上面，y=300 在下面（左上角是原点）
    #   翻转后 → y 轴向下，跟 OpenCV / 图片坐标系一致
    #   否则框的 y 坐标会反着画
    ax.invert_yaxis()

    ax.set_title(title, fontweight="bold")

    # ── 遍历 8 个框，逐个画矩形 ──
    # enumerate(boxes) → i=0~7 (索引), b=每行的 6 个数
    for i, b in enumerate(boxes):
        x1, y1, x2, y2 = b[:4]  # 只取前 4 个坐标值

        if i in keep:
            # ═════════════════════════════════════════════════════════
            # 情况 A: 框在保留名单里 → 画彩色实线
            # ═════════════════════════════════════════════════════════
            # patches.Rectangle: 在 matplotlib 画布上画一个矩形
            #   参数:
            #     (x1, y1)          → 矩形左上角坐标
            #     x2-x1             → 宽度
            #     y2-y1             → 高度
            #     edgecolor="blue"  → 边框颜色（猫=蓝，狗=橙）
            #     facecolor="none"  → 不填充内部，只画边框
            #     linewidth=2       → 线宽 2 像素（比删掉的粗一倍）
            rect = patches.Rectangle((x1, y1), x2 - x1, y2 - y1,
                                      linewidth=2,
                                      edgecolor=colors[int(b[5])],
                                      facecolor="none")
            ax.add_patch(rect)  # add_patch: 把矩形"贴"到子图上

            # ax.text: 在框的左上角上方写文字
            #   参数: (x坐标, y坐标, "文字", fontsize=字号, color=颜色)
            #   y1-5 = 比框的顶部高 5 像素 → 文字在框上方，不压住框
            #   内容: "猫 0.95" / "狗 0.91"  ← 类别名 + 置信度
            ax.text(x1, y1 - 5, f"{names[b[5]]} {b[4]:.2f}",
                    fontsize=8, color=colors[b[5]])
        else:
            # ═════════════════════════════════════════════════════════
            # 情况 B: 框被干掉了 → 画灰色虚线
            # ═════════════════════════════════════════════════════════
            # edgecolor="gray"    → 灰色边框 = 被淘汰的标记
            # linewidth=1         → 细线，不抢眼
            # linestyle="--"      → 虚线，一眼看出是"已经不存在的框"
            rect = patches.Rectangle((x1, y1), x2 - x1, y2 - y1,
                                      linewidth=1,
                                      edgecolor="gray",
                                      facecolor="none",
                                      linestyle="--")
            ax.add_patch(rect)

    # ── 每张子图左下角标注统计 ──
    # 位置 (10, 275): x=10(靠左), y=275(靠下)
    # bbox=dict(...): 给文字画一个圆角背景框 → 更醒目
    ax.text(10, 275, f"保留 {len(keep)}/{len(boxes)} 框",
            bbox=dict(boxstyle="round", facecolor="wheat"))

# ── 全局收尾 ──
plt.tight_layout()  # 自动调整子图间距，避免重叠
plt.show()          # 弹出窗口，显示 3 张对比图
print("✅ 关卡 3 PyTorch版 完成 — 手写 NMS + IoU + Soft-NMS, 全不依赖框架")
